package ro.fortsoft.mappings;

import javax.persistence.Column;
import javax.persistence.MappedSuperclass;

/**
 * @author Daniel Jipa
 */
@MappedSuperclass
public abstract class DefaultNomenclator extends DefaultPersistent implements Nomenclator {

    private static final long serialVersionUID = 8384174412873639825L;

    @Column(name = "NAME")
    private String name;

    @Column(name = "DESCRIPTION")
    private String description;

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(final String description) {
        this.description = description;
    }

    @Override
    public String toString() {
        return "DefaultNomenclator [name=" + name + ", description="
                + description + "]";
    }


}
